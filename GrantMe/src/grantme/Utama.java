    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String Nama_beasiswa = txtNamaBeasiswa.getText().toString().trim();
        String Jumlah_kuota = txtJumlah.getText().toString().trim();
        String dalam_negeri = rdDalamNegeri.getText().toString().trim();
        String luar_negeri = rdLuarNegeri.getText().toString().trim();
        if (dalam_negeri == null) {
            try {
                Connection c = Koneksi.getKoneksi();
                String sql = "INSERT INTO beasiswa_luar_negeri VALUES(?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, "0");
                p.setString(2, Nama_beasiswa);
                p.setString(3, Jumlah_kuota);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null, "Beasiswa berhasil ditambahkan!");

            }catch (SQLException e) {
                System.out.println("Tambah Beasiswa eror");
            }
        }else{
            try {
                Connection c = Koneksi.getKoneksi();
                String sql = "INSERT INTO beasiswa_dalam_negeri VALUES(?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, "0");
                p.setString(2, Nama_beasiswa);
                p.setString(3, Jumlah_kuota);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null, "Beasiswa berhasil ditambahkan!");

            }catch (SQLException e) {
                System.out.println("Tambah Beasiswa eror");
            }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

