    private void btnEditDlmNegeriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditDlmNegeriActionPerformed
        // TODO add your handling code here:
        int i = tblDlmNegeri.getSelectedRow();
        if (i == -1) {
// tidak ada baris terseleksi return;
        }
// ambil id yang terseleksi
        String id = (String) model.getValueAt(i, 0);
        String Nama_beasiswa = txtNamaDalam.getText();
        String Jumlah_kuota = txtJumlahDalam.getText();
        
        try {
            Connection c = Koneksi.getKoneksi();
            String sql = "UPDATE beasiswa_dalam_negeri SET nama_beasiswa =?, jumlah_kuota =? WHERE id_dalam_negeri = ?";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, Nama_beasiswa);
            p.setString(2, Jumlah_kuota);
            p.setString(3, id);
            p.executeUpdate();
            p.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error update dalam negeri");
        } finally{
            tabel_dlm_negeri();
        }
    }//GEN-LAST:event_btnEditDlmNegeriActionPerformed

