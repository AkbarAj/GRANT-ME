    public void loadData1() {
        // menghapus seluruh data
        model.getDataVector().removeAllElements();
        // memberi tahu bahwa data telah kosong
        model.fireTableDataChanged();
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM beasiswa_dalam_negeri";
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                // lakukan penelusuran baris
                Object[] o = new Object[3];
                o[0] = r.getString("id_dalam_negeri");
                o[1] = r.getString("nama_beasiswa");
                o[2] = r.getString("jumlah_kuota");
                
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error tabel dalam negeri");
        }
    }

